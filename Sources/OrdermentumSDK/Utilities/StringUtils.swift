import Foundation

extension String {
    func toBool() -> Bool {
        switch self {
        case "True", "true", "yes", "1":
            return true
        case "False", "false", "no", "0":
            return false
        default:
            return false
        }
    }
}

extension Data {
    func utf8String() -> String {
        return String(data: self, encoding: .utf8) ?? ""
    }
}
